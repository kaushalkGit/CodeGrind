let arr=[1,2,3,4,5]
/*
Map
let newArr=arr.map((item,index,array)=>{
   return item+5
})
console.log(newArr)
*/
/*
Filter
let newArr=arr.filter((item,index,array)=>{
   return item>2
})
console.log(newArr)
*/
/*
Reduce
let sum=arr.reduce((prev,cur)=>{
   return prev+cur
},5)
console.log(sum)
*/
